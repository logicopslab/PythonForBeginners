# A python program to demonstrate the use of
# max(), min()

tup_1 = ('LogicOps', 'Lab')
tup_2 = (10, 1)

print ('Maximum element in tuples 1,2: ' +
		str(max(tup_1)) + ', ' +
		str(max(tup_2)))
print ('Minimum element in tuples 1,2: ' +
	str(min(tup_1)) + ', ' + str(min(tup_2)))

# len() function

tup_3, tup_4 = (12, 'xy', 'za','rm'), (4567, 'abcd')
print ("First tuple length : ", len(tup_3))
print ("Second tuple length : ", len(tup_4))


