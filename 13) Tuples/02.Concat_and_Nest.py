# Code for concatenating 2 tuples

tup_1 = (0, 2, 4, 8)
tup_2 = ('Logic', 'Ops', 'Lab')

# Concatenation

print(tup_1 + tup_2)

# Creating nested tuples

tup_1 = (0, 2, 4, 8)
tup_2 = ('Logic', 'Ops', 'Lab')
tup_3 = (tup_1, tup_2)
print(tup_3)

# Tuple with repetition

tup_4 = ('python',)*4
print(tup_4)
