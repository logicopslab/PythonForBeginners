
MyList = ['L','O','G','I','C','_','O','P','S','_','L','A','B']
        
print("Initial MyList: ")
print(MyList)

List_After_Slicing = MyList[3:8]
print("\nSlicing elements in a range 3-8: ")
print(List_After_Slicing)

List_After_Slicing = MyList[5:]
print("\nElements sliced from 5th element till the end: ")
print(List_After_Slicing)

List_After_Slicing = MyList[:]
print("\nPrinting all elements using slice operation: ")
print(List_After_Slicing)
