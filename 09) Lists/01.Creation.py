# Creation

MyList = []
print("This is a blank List: ")
print(MyList)

MyList0 = [2, 4, 'Logic', 6, 'Ops', 8, 'Lab']
print("\nList with the use of Mixed Values: ")
print(MyList0)

# List of numbers
MyList1 = [100, 200, 140, 400]
print("\nList of the numbers: ")
print(MyList1)

# List of strings and using index to access
MyList2 = ["Logic", "Ops", "Lab"]
print("\nItems of the List: ")
print(MyList2[0])
print(MyList2[2])
print(MyList2[-1])
print(MyList2[-2])

# Creating a Multi-Dimensional List
# (By Nesting a list inside a List)
MDList = [['Logic', 'Ops'] , ['Lab']]
print("\nHere is the Multi-Dimensional List: ")
print(MDList)

print("Accessing a element from a Multi-Dimensional list")
print(MDList[1][0])
print(MDList[0][1])
